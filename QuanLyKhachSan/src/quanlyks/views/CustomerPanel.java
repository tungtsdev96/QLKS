    public static void bindingTableCustomer(ArrayList<Customer> arrCustomer) {
        DefaultTableModel model = (DefaultTableModel) tbCustomer.getModel();
        model.setRowCount(0);
        for (int i = 0; i < arrCustomer.size(); i++) {
            model.addRow(new Object[]{
                arrCustomer.get(i).getIdCustomer(),
                arrCustomer.get(i).getNameCustomer(),
                arrCustomer.get(i).getSex(),
                arrCustomer.get(i).getPhoneNumber(),
                arrCustomer.get(i).getNational(),});
        }
        tbCustomer.setModel(model);
    }

    public static void analysisCustomer() {
        StringBuilder builer = new StringBuilder();
        builer.append("Thống kê khách hàng\n");
        ArrayList<String> arrNational = customerController.getListNational();
        for (int i = 0; i < arrNational.size(); i++) {
            builer.append(arrNational.get(i) + ": ");
            int number = customerController.countCustomerByNational(arrNational.get(i));
            builer.append(number + "\n");
        }
        taAnalysis.setText(builer.toString());
    }

    public void querySearch() {
        String query = TSearch.getText();
        arrCustomers = customerController.searchCustomer(query);
        if (arrCustomers.size() != 0) {
            bindingTableCustomer(arrCustomers);
            iconSearch.setIcon(icon.iconBack());
            checkSearch = true;
        } else {
            JOptionPane.showMessageDialog(this, "Không có kết quả phù hợp", "Tìm kiếm", JOptionPane.INFORMATION_MESSAGE, icon.iconError());
            iconSearch.setIcon(icon.iconSearch());
            checkSearch = false;
        }
    }

