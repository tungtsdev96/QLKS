    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tbCustomer.getSelectedRow();
        if (row != -1) {
            String idCus = tbCustomer.getValueAt(row, 0).toString();
            int output = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa khách hàng " + idCus + "?", "Xóa khách hàng", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, icon.iconDelete());
            if (output == JOptionPane.YES_OPTION) {
                if (customerController.deleteCustomer(idCus)) {
                    JOptionPane.showMessageDialog(this, "Xóa thành công", "Xóa", JOptionPane.INFORMATION_MESSAGE, icon.iconDone());
                    bindingTableCustomer(customerController.getListCustomer());
                    analysisCustomer();
                } else {
                    JOptionPane.showMessageDialog(this, "Chưa xóa được", "Lỗi", JOptionPane.ERROR_MESSAGE, icon.iconError());
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chưa chọn mục nào!", "Trống", JOptionPane.ERROR_MESSAGE, icon.iconError());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

