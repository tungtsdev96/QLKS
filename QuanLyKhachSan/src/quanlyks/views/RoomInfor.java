    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:
        if (type == 2) {
            boolean checkSuccess = false;
            if (edtIdRoom.getText().isEmpty() || edtPriceRoom.getText().isEmpty() || (Integer) spMaxOccupancy.getValue() == 0) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!", "Lỗi", JOptionPane.ERROR_MESSAGE, icon.iconError());
            } else {
                checkSuccess = addRoom();
                if (checkSuccess == true && checkInsert == true) {
                    arrRoom = roomController.getListRooms();
                    parent.bindingRoomTable(arrRoom);
                    parent.getAnalysisRoom(roomController);
                    JOptionPane.showMessageDialog(this, "Đã thêm", "Thành công", JOptionPane.INFORMATION_MESSAGE, icon.iconDone());
                    MainScreen.panelContents.getComponent(0).repaint();
                    this.setVisible(false);
                } else if (checkSuccess == true && checkInsert == false) {
                    arrRoom = roomController.getListRooms();
                    parent.bindingRoomTable(arrRoom);
                    parent.getAnalysisRoom(roomController);
                    JOptionPane.showMessageDialog(this, "Đã chỉnh sửa", "Thành công", JOptionPane.INFORMATION_MESSAGE, icon.iconDone());
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(this, "Đã có lỗi xảy ra!", "Lỗi", JOptionPane.ERROR_MESSAGE, icon.iconError());
                }
            }
        }

    }//GEN-LAST:event_btnOkActionPerformed

