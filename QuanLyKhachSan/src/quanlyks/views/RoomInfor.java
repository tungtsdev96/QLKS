    public RoomInfor(RoomPanel parent, Room room, int type) {
        if (room != null) {
            initComponents();
            this.parent = parent;
            this.type = type;
            edtIdRoom.setText(room.getIdRoom() + "");
            String roomType = room.getRoomType();
            cbxRoomType.setSelectedItem(roomType);
            String roomStatus = room.getRoomStatus();
            cbxRoomStatus.setSelectedItem(roomStatus);
            edtPriceRoom.setText(room.getPriceOfRoom() + "");
            spMaxOccupancy.setValue(room.getMaxOccupancy());
            // Nếu ấn nút xem chi tiết thì type = 1
            // Nếu ấn nút chỉnh sửa type = 2
            if (type == 1) {
                edtIdRoom.setEditable(false);
                edtPriceRoom.setEditable(false);
                cbxRoomStatus.setEnabled(false);
                cbxRoomType.setEnabled(false);
                spMaxOccupancy.setEnabled(false);
            } else if (type == 2) {
                checkInsert = false;
                cbxRoomStatus.setEditable(false);
                edtIdRoom.setEditable(false);
                DefaultComboBoxModel mode = new DefaultComboBoxModel();
                mode.addElement(roomStatus);
                mode.addElement("Đang sửa chữa");
                cbxRoomStatus.setModel(mode);
            }

        } else {
            this.setVisible(false);
        }

    }

    private boolean addRoom() {
        int idRoom = 0;
        String typeRoom = cbxRoomType.getSelectedItem().toString();
        String statusRoom = cbxRoomStatus.getSelectedItem().toString();
        int maxOccupancy = (Integer) spMaxOccupancy.getValue();
        long price = 0;
        try {
            idRoom = Integer.parseInt(edtIdRoom.getText());
            price = Long.parseLong(edtPriceRoom.getText());
            Room room = new Room(idRoom, typeRoom, statusRoom, maxOccupancy, price);
            if (checkInsert == true) {
                return roomController.addRoom(room);
            } else {
                return roomController.updateRoom(room);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Kiểm tra lại thông tin về Mã phòng và Giá tiền", "Lỗi", JOptionPane.ERROR_MESSAGE, icon.iconError());
            return false;
        }

    }

